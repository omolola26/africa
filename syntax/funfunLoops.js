// loop ==> start, stop, step
// start = 30
// stop = 50
// step = 1
// step = 2
// step = 5


// for loop
/**
 *  for ( start, stop, step ){
 *      // perform action
 * 
 *  }
 */

// for (let i = 30; i <= 50; i += 3) {
//     console.log(i)
// }

for (let x = 88; x > 20; x -= 5) {
    console.log(x)
}


// infinite loop
/**
 * hoard your system resources 
 */
for (let y = 47; y > 2; y--) {
    console.log(y)
}

// while

/**
 *  start
 *  while (stop){
 * 
 *   step
 *  }
 */

let g = 30 // start
while (g < 50) { // stop
    console.log(g)
    g++ // step
}
